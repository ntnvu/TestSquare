public class InputSquare {
	int a;
	int b;
	int d;
	
	public InputSquare() {
		super();
	}

	public InputSquare(int a, int b, int d) {
		super();
		this.a = a;
		this.b = b;
		this.d = d;
	}

	public int getA() {
		return a;
	}

	public void setA(int a) {
		this.a = a;
	}

	public int getB() {
		return b;
	}

	public void setB(int b) {
		this.b = b;
	}

	public int getD() {
		return d;
	}

	public void setD(int d) {
		this.d = d;
	}
}
	
	
	
	